package com.seleniumGo.warmup;

public class StringConcatination {
	public static void main(String arfg[]) {
		int a = 100;
		int b = 200;
		String x = "Hello";
		String y = "jaydeep";
		System.out.println(a+b);
		System.out.println(x+y);
		System.out.println(a+b+x+y);
		System.out.println(x+y+a+b);
		System.out.println(x+y+(a+b));
	}
}
