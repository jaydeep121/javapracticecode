package com.seleniumGo.warmup;

public class FinalKey {
	public static void main(String arg[]) {
		/*use of final keyword is :
		 * To create constant variable : final variable
		 * To prevent method for over riding : final method
		 * To prevent inheritance : final class
		 * */
		final int i=10;
		System.out.println(i);
		
	}

}
